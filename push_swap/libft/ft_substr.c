#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	sizlen;
	char	*str;

	sizlen = ft_strlen(s);
	if (!s || start >= sizlen || len == 0)
	{
		str = (char *)malloc(1);
		if (!str)
			return (NULL);
		str[0] = '\0';
		return (str);
	}
	if (len > sizlen - start)
		len = sizlen - start;
	str = (char *)malloc(len + 1);
	if (!str)
		return (NULL);
	ft_memcpy(str, s + start, len);
	str[len] = '\0';
	return (str);
}
